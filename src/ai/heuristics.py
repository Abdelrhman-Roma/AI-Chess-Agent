# ============================================  
# Heuristic 1: Basic Material (300-500 ELO)  # تقييم بسيط بالماتريال فقط
# ============================================  
def heuristics_1(board_obj):  # دالة التقييم الأولى
    values = {  # قيمة كل نوع قطعة
        "p": 100,  # قيمة البيدق
        "n": 320,  # قيمة الحصان
        "b": 330,  # قيمة الفيل
        "r": 500,  # قيمة القلعة
        "q": 900,  # قيمة الوزير
        "k": 0,  # قيمة الملك صفر لأن خسارته بتتحسب بالمات مش بالنقاط
    }  # نهاية جدول القيم

    score = 0  # بنبدأ السكور من صفر

    for row in board_obj.board:  # بنلف على كل صف
        for piece in row:  # بنلف على كل قطعة في الصف
            if piece:  # لو فيه قطعة
                val = values[piece[1]]  # بنجيب قيمة نوع القطعة
                score += val if piece[0] == "w" else -val  # الأبيض موجب والأسود سالب

    return score  # بنرجع السكور النهائي


# ============================================  
# Heuristic 2: Material + Position (600-1000 ELO)  # تقييم بالماتريال + المكان
# ============================================  
def heuristics_2(board_obj):  # دالة التقييم الثانية
    board = board_obj.board  # بنجيب البورد

    values = {  # قيمة كل قطعة
        "p": 100,  # قيمة البيدق
        "n": 320,  # قيمة الحصان
        "b": 330,  # قيمة الفيل
        "r": 500,  # قيمة القلعة
        "q": 900,  # قيمة الوزير
        "k": 0,  # الملك صفر
    }  # نهاية القيم

    pawn_table = [  # جدول أفضلية أماكن البيادق
        [0, 0, 0, 0, 0, 0, 0, 0],  # الصف الأول
        [50, 50, 50, 50, 50, 50, 50, 50],  # الصف الثاني
        [10, 10, 20, 30, 30, 20, 10, 10],  # الصف الثالث
        [5, 5, 10, 25, 25, 10, 5, 5],  # الصف الرابع
        [0, 0, 0, 20, 20, 0, 0, 0],  # الصف الخامس
        [5, -5, -10, 0, 0, -10, -5, 5],  # الصف السادس
        [5, 10, 10, -20, -20, 10, 10, 5],  # الصف السابع
        [0, 0, 0, 0, 0, 0, 0, 0],  # الصف الثامن
    ]  # نهاية جدول البيادق

    knight_table = [  # جدول أفضلية أماكن الحصان
        [-50, -40, -30, -30, -30, -30, -40, -50],  # صف
        [-40, -20, 0, 0, 0, 0, -20, -40],  # صف
        [-30, 0, 10, 15, 15, 10, 0, -30],  # صف
        [-30, 5, 15, 20, 20, 15, 5, -30],  # صف
        [-30, 0, 15, 20, 20, 15, 0, -30],  # صف
        [-30, 5, 10, 15, 15, 10, 5, -30],  # صف
        [-40, -20, 0, 5, 5, 0, -20, -40],  # صف
        [-50, -40, -30, -30, -30, -30, -40, -50],  # صف
    ]  # نهاية جدول الحصان

    bishop_table = [  # جدول أفضلية أماكن الفيل
        [-20, -10, -10, -10, -10, -10, -10, -20],  # صف
        [-10, 0, 0, 0, 0, 0, 0, -10],  # صف
        [-10, 0, 5, 10, 10, 5, 0, -10],  # صف
        [-10, 5, 5, 10, 10, 5, 5, -10],  # صف
        [-10, 0, 10, 10, 10, 10, 0, -10],  # صف
        [-10, 10, 10, 10, 10, 10, 10, -10],  # صف
        [-10, 5, 0, 0, 0, 0, 5, -10],  # صف
        [-20, -10, -10, -10, -10, -10, -10, -20],  # صف
    ]  # نهاية جدول الفيل

    tables = {  # ربط نوع القطعة بالجدول بتاعها
        "p": pawn_table,  # جدول البيادق
        "n": knight_table,  # جدول الحصان
        "b": bishop_table,  # جدول الفيل
    }  # نهاية الربط

    score = 0  # بنبدأ السكور من صفر

    for r in range(8):  # بنلف على الصفوف
        for c in range(8):  # بنلف على الأعمدة
            piece = board[r][c]  # بنجيب القطعة
            if not piece:  # لو مفيش قطعة
                continue  # نكمل

            color = piece[0]  # لون القطعة
            ptype = piece[1]  # نوع القطعة

            val = values[ptype]  # قيمة القطعة
            score += val if color == "w" else -val  # نضيف الماتريال

            if ptype in tables:  # لو القطعة ليها جدول مكاني
                table_row = 7 - r if color == "w" else r  # بنحدد الصف المناسب حسب اللون
                positional_score = tables[ptype][table_row][c]  # بنجيب السكور المكاني
                score += positional_score if color == "w" else -positional_score  # نضيفه مع الإشارة المناسبة

    return score  # بنرجع السكور النهائي


# ============================================  
# Heuristic 3: Advanced (1000-1500 ELO)  # تقييم متقدم شوية
# ============================================  
def heuristics_3(board_obj):  # دالة التقييم الثالثة
    board = board_obj.board  # بنجيب البورد

    values = {  # قيم القطع
        "p": 100,  # البيدق
        "n": 320,  # الحصان
        "b": 330,  # الفيل
        "r": 500,  # القلعة
        "q": 900,  # الوزير
        "k": 0,  # الملك
    }  # نهاية القيم

    center_squares = {(3, 3), (3, 4), (4, 3), (4, 4)}  # مربعات السنتر المهمة

    material = 0  # سكورس الماتريال
    center = 0  # سكورس السيطرة على السنتر
    bishop_pair = {"w": 0, "b": 0}  # عدد الفيلة لكل لون
    pawn_penalty = 0  # خصم البيادق المضاعفة

    white_pawns_by_file = [0] * 8  # عداد بيادق الأبيض في كل عمود
    black_pawns_by_file = [0] * 8  # عداد بيادق الأسود في كل عمود

    for r in range(8):  # بنلف على الصفوف
        for c in range(8):  # بنلف على الأعمدة
            piece = board[r][c]  # بنجيب القطعة
            if not piece:  # لو مفيش قطعة
                continue  # نكمل

            color = piece[0]  # اللون
            ptype = piece[1]  # النوع

            val = values[ptype]  # قيمة القطعة
            material += val if color == "w" else -val  # نحسب الماتريال

            if (r, c) in center_squares:  # لو القطعة في السنتر
                center += 20 if color == "w" else -20  # ندي بونس أو خصم

            if ptype == "b":  # لو القطعة فيل
                bishop_pair[color] += 1  # نزود عداد الفيلة

            if ptype == "p":  # لو القطعة بيدق
                if color == "w":  # لو أبيض
                    white_pawns_by_file[c] += 1  # نزود عداد العمود
                else:  # لو أسود
                    black_pawns_by_file[c] += 1  # نزود عداد العمود

    for count in white_pawns_by_file:  # بنلف على أعمدة الأبيض
        if count > 1:  # لو فيه بيادق مضاعفة
            pawn_penalty -= 10 * (count - 1)  # نخصم من الأبيض

    for count in black_pawns_by_file:  # بنلف على أعمدة الأسود
        if count > 1:  # لو فيه بيادق مضاعفة
            pawn_penalty += 10 * (count - 1)  # نضيف لأن ده سيء للأسود

    bishop_score = 0  # بونس الفيلين

    if bishop_pair["w"] >= 2:  # لو الأبيض عنده زوج فيلة
        bishop_score += 30  # ياخد بونس
    if bishop_pair["b"] >= 2:  # لو الأسود عنده زوج فيلة
        bishop_score -= 30  # يتخصم من منظور الأبيض

    return material + center + bishop_score + pawn_penalty  # بنرجع السكور النهائي
